print("Informe dois números para calcularmos as operações aritméticas simples")
print()

x = float(input("Qual o primeiro número? "))
# 4.0
y = float(input("Qual o segundo número? "))
# 3.0

# Imprimir uma linha
print()
print(x, "+", y, "=", (x+y))
# 4.0 + 3.0 = 7.0
print(x, "-", y, "=", (x-y))
print(x, "x", y, "=", (x*y))
print(x, "÷", y, "=", (x/y))